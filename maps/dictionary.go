package maps

const (
	ErrNotFound = DictionaryErr("word not found")
	ErrWordExist = DictionaryErr("word already exist")
	ErrWordDoesNotExist = DictionaryErr("word does not exist so it can't be updated")
	ErrWordDoesNotExistDelete = DictionaryErr("word does not exist so it can't be deleted")
)

type DictionaryErr string

func (e DictionaryErr) Error() string {
	return string(e)
}

type Dictionary map[string]string

func (d Dictionary) Search(word string) (string, error) {
	definition, ok := d[word]
	if !ok {
		return "", ErrNotFound
	}
	return definition, nil
}

func (d Dictionary) Add(word, definition string) error {
	_, err := d.Search(word)

	switch err {
	case ErrNotFound:
		d[word] = definition
	case nil:
		return ErrWordExist
	default:
		return err
	}

	return nil
}

func (d Dictionary) Update(word, definition string) error {
	_, err := d.Search(word)

	switch err {
	case ErrNotFound:
		return ErrWordDoesNotExist
	case nil:
		d[word] = definition
	default:
		return err
	}

	return nil
}

func (d Dictionary) Delete(word string) error {
	_, err := d.Search(word)

	switch err {
	case ErrNotFound:
		return ErrWordDoesNotExistDelete
	case nil:
		delete(d, word)
	default:
		return err
	}

	return nil
}